#Empty Dictionary

my_dict = {}

#Dictionary of Integers

my_dict = {1:'apple', 2:'ball'}
print(my_dict)

#Dictionary of mixed keys

my_dict = {'name':'John', 1:[2,4,3]}
print(my_dict)
#dict = {'Name':'joe', 1:'class'}
