#We can also creat a DICTIONARY using the built-in 'dict() function'
#using dict()

my_dict = dict({1:'apple', 2:'ball'})
print(my_dict)

#From SEQUENCE having each item as a pair

my_dict = dict([(1,'apple'),(2,'ball')])
print(my_dict)